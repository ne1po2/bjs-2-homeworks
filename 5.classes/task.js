class Library {
  constructor(name) {
    this.name = name;
    this.books = [];
  }

  addBook(book) {
    if (book.state > 30) {
      this.books.push(book);
    }
  }

  findBookBy(type, value) {
    for (const book of this.books) {
      if (book[type] == value) {
        return book;
      }
    }
    return null;
  }

  giveBookByName(bookName) {
    for (let i = 0; i < this.books.length; i++) {
      if (this.books[i].name === bookName) {
        return this.books.splice(i, 1)[0];
      }
    }
    return null;
  }
}

class Student {
  constructor(name) {
    this.name = name;
    this.marks = {};
  }

  addMark(mark, subject) {
    if (mark < 2 || mark > 5) {
      return;
    }
    if (!this.marks[subject]) {
      this.marks[subject] = [];
    }
    this.marks[subject].push(mark);
  }

  getAverageBySubject(subject) {
    if (!this.marks[subject]) {
      return 0;
    }
    let sum = this.marks[subject].reduce((sum, mark) => sum + mark, 0);
    return sum / this.marks[subject].length;
  }

  getAverage() {
    let listSubjects = Object.keys(this.marks);
    if (listSubjects.length == 0) {
      return 0;
    }
    let sum = 0;
    for (let i = 0; i < listSubjects.length; i++) {
      sum += this.getAverageBySubject(listSubjects[i]);
    }
    return sum / listSubjects.length;
  }
}